package estrutura.sequencial;

public class Processamento {

	public static void main(String[] args) {

		int a, b, d;
		double resultado, c;
		
		a = 5;
		b = 2;
		c = 5.0;
		
		// Casting
		resultado = (double) a / b;
		d = (int) c;
		
		System.out.println(resultado);
		System.out.println(d);
	}

}
